'''
Exercício 6: Calculando a média
Crie três variáveis chamadas nota1, nota 2 e nota 3 e atribua valores numéricos a elas.
Calcule a média dessas notas e armazene o resultado em media.
Por fim, exiba a seguinte mensagem formatada:
"A média das notas [nota1], [nota2] e [nota3] é [media]."
'''

nota1 = 6
nota2 = 7
nota3 = 9
#As operações matemáticas obedecem à regra PEMDAS
